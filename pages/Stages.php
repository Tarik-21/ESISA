                <p>Le stage est une part fondamentale de la formation d'un ingénieur. Il a pour but de placer le futur ingénieur dans un cadre opérationnel. Le stage s'effectue sur le site d'une entreprise et porte sur un sujet limité d'un niveau ingénieur, c'est à dire qu'il doit comporter au moins une partie étude. <br><br>
                    Pour l'élève ingénieur, le stage doit avoir une fonction pédagogique et doit être une expérience enrichissante. Si le stagiaire met toute sa motivation à réussir ses premiers pas dans la vie professionnelle, l'entreprise peut également tirer profit de la formation qu'il dispense. <br><br>
                    Les explications que le maître de stage doit fournir nécessitent de sa part une prise de recul par rapport à son activité. D'autre part le côté "candide" du stagiaire peut inciter l'entreprise à analyser la pertinence ou l'opportunité de certaines de ses "habitudes" de travail. <br><br>
                    Enfin le stagiaire peut être un apport important pour les entreprises par ses compétences théoriques et techniques acquises au cours de sa formation à l'école. <br><br>
                    Le stagiaire est suivi durant cette période par un maître de stage au sein de l'entreprise et par un tuteur au sein de l'ESISA. Leur rôle est de suivre l'évolution du stage et du stagiaire et de garantir la qualité du stage. Le stage donne lieu à un rapport et une soutenance. La note de stage correspond à une triple évaluation, sur le travail effectué, sur le rapport écrit et sur l'oral. La rubrique stages vous permet de déposer vos offres qui seront diffusées auprès des élèves d'ingénieur concernés. <br><br>
                    La note sur le travail réalisé est donnée par le maître de stage. Cette note doit tenir compte des résultats techniques, de l'esprit d'initiative et d'autonomie, enfin de son insertion et du travail en équipe. La note sur le rapport est donnée conjointement par le maître de stage et le tuteur. Elle prend en considération l'organisation du document, l'équilibre entre les différents chapitres, la qualité technique et l'utilisation potentielle de ce rapport pour les besoins internes de l'entreprise. La note d'oral est donnée conjointement par le maître de stage et le tuteur. Elle tient compte de l'organisation de la présentation, de l'aisance du stagiaire et de la qualité technique des réponses aux questions. La note finale du stage est la moyenne de ces trois notes. <br><br>
                    Vous pouvez déposer vos offres de stages en envoyant un courrier à <span class="fw-bold">stages@esisa.ac.ma</span>  <br><br>
                
