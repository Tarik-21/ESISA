                      <p>e-Medisys est un e nouvelle conférence internationale, très novatrice, sur le thème de la télémédecine. Cette conférence est née de la collaboration de trois équipes de recherche réparties entre l’Université de Sfax-Tunis, l’Universités de Franche Comté-Besançon-France et l’Ecole Supérieure d’Ingénierie en Sciences Appliquées ESISA-Fès Maroc. Qu'il s'agisse d'une expérience de télémédecine entre praticiens ou d'articles plus techniques pour des informaticiens, il est vraiment intéressant que nos communautés échangent les points de vue. e-Medisys, de par son thème transdisciplinaire, a pour vocation de réunir les chercheurs, mais aussi les industriels, qui sont acteurs de la télémédecine tant du point de vue médical que du point de vue informatique. C'est ce qui en fait un évènement unique : la réunion des acteurs qui permettront de traiter la télémédecine "de bout en bout". Cette conférence s'est tenue en français et en anglais. Les papiers ont été sélectionnés par un comité de lecture mixte regroupant des spécialistes de renommée dans le domaine de la télémédecine et les nouvelles technologies de l'information. Pour la session de tutoriaux, nous avons invité des sommités comme le Professeur Maresco (Opération Lindberg, argonaute3D), le Professeur Bashshur (Journal on e-health and telemedecine) ou encore le Professeur Lareng (Président de la Société Européenne de Télémédecine) et d'autres sommités dans le domaine de la recherche.</p>
