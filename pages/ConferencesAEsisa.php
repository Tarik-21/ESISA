                            <h2>Nouveaux Défis de l’Internet Des Objets. 18 et 19 MAI 2017, Fès MAROC</h2>
                            <p>Suite à la journée nationale organisée le 29 Novembre 2016 à la Cite´ de l’Innovation, Nokia Paris-Saclay (https://ido2016.sciencesconf.org/), nous souhaitons poursuivre l’exploration des nouveaux défis que l’Internet des Objets (IdO) lors d’une conférence de deux jours qui aura lieu à Fès au Maroc dans les locaux de l’Ecole Supérieure d’Ingénierie en Sciences Appliquées (ESISA). Cette conférence interrogera les problématiques de l’IdO suivant trois axes complémentaires technique, usage, éthique. Les techniques utilisées pour produire les artefacts (objets physiques, infrastructures), les programmes (algorithmiques, logiciels) et les données (Big Data, linked data, metadata, ontologies) font l’objet de multiple innovations tant le domaine de l’IdO est riche et stimulant.</p>
