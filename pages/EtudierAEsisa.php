                <h2>DES OBJECTIFS AMBITIEUX </h2>
                <p>L'ESISA est la grande école d'ingénierie en informatique. Nos diplômés auront vocation à exercer des fonctions d'ingénieurs d'études, de conception et de développement, etc. <br>

                    L'école s'attache à familiariser ses étudiants aux outils modernes de l'ingénieurie, tels que : le travail en groupe et la conduite de projet, etc. Cette volonté se traduit par l'importance donnée aux stages en entreprise : <br>
                    
                    Un stage "Méthodes Informatiques" de 2 à 3 mois à la fin de la 3ème année et un stage "Etude et conception" de 4 à 6 mois à la fin de la 5ème année, qui feront l'objet d'une soutenance de mémoire.</p>
