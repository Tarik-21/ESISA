                <p>Les systèmes et réseaux informatiques font désormais partie intégrante de tous les domaines des technologies de l'information. L’ingénierie des réseaux est maintenant une discipline bien établie qui intègre à la fois les connaissances théoriques des réseaux, que pratiques : Techniques de conception et méthodes de mise en oeuvre. <br><br>
                    La certification académique CCNA (Cisco Certified Networking Academy) compte environ 80h de formation (cours, travaux pratiques, Examens) et s’adresse aux personnes désirant acquérir une formation solide dans le domaine des réseaux informatiques, l’interconnexion des réseaux et la mise en oeuvre de réseaux informatiques. Aucune connaissance dans le domaine des réseaux n’est requise. <br><br>
                    Des certificats académiques Cisco seront délivrés aux stagiaires à la fin de la formation ainsi qu’une attestation de formation délivrée par la direction de l’ESISA. <br><br>
                    Les cours seront animés par des experts du domaine certifiés en tant qu’instructeur sur les produits objets de cette formation. Les cours sont composés de plusieurs chapitres. Chaque chapitre commencera par un exposé théorique, où le formateur explique les fondements ou les fonctionnalités étudiées. Ces exposés seront appuyés par des manipulations pratiques par les stagiaires afin qu’ils assimilent au mieux les concepts vus dans le cours. <br><br>
