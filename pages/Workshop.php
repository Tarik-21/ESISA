                            <h2>WORKSHOP DU 12 MARS 2011 SOUS LE THÈME : LA FORMATION AU SERVICE DE L'ENTREPRISE</h2>
                            <img src="./images/conférence_esisa/workshop11 .jpg" alt="img" style="width: 60%;">
                            <h2 class="mt-2">Thématiques</h2>
                            <p>La journée a pour objectif de valoriser les recherches et les réalisations basées sur les technologies numériques et les objets connectés et leurs usages quotidiens .<br> <br>

                                Dans le cadre de son orientation stratégique visant l'amélioration de la qualité de la formation fournie à ses élèves ingénieurs, l'école ESISA (Ecole Supérieure d'Ingénierie en Sciences Appliquées) a organisé le 12 Mars 2011 une journée d'étude à l'Hôtel Jnane Palace FES sous le thème : <br> <br>
                                La Formation au Service de l'Entreprise <br> <br>
                                La journée vise à réunir un public constitué à la fois d'entreprises actives dans le domaine du développement offshore, élèves ingénieurs, formateurs experts en technologies de l'information et de la communication et l' ESISA qui a participé depuis l'an 1999 dans la formation de jeunes ingénieurs confirmés, déjà actifs au niveau national et international dans les domaines des Technologies Avancées, Offshore & TIC et Recherche & Développement. <br> <br>
                                Les objectifs de la journée se résument principalement dans les points suivants : <br> <br>
                                <span class="fw-bold">
                                    - Sensibilisation à la nécéssité de la bonne formation avant et après le recrutement. <br>
                                    - La sensibilisation des institutions d'enseignement supérieur, d'adapter les programmes aux besoins de l'entreprise. <br>
                                    - Participation au concours de recrutement et aux formations pour l'insertion. <br> 
                                    - Participation aux formations des collaborateurs des entreprises actives dans le domaine du développement offshore et autre. <br>
                                    - Les formations en alternance. <br>
                                    - Créer une synergie entre les chercheurs et les industriels pour la promotion de la recherche et développement. <br>
                                </span>
