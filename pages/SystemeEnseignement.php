                <p>L'enseignement à l'école ESISA est fondé sur un fonctionnement par semestre, basé sur le système modulaire. A l'école ESISA l'enseignement est dispensé en 2 cycles étalés sur 5 années d'études : <br> <br> </p>
                <ul class="ps-5 fw-bold">
                    <li>1er Cycle (Bac +3 ans)</li>
                    <li>2ème Cycle (2 ans après Bac +3) M1 et M2, à l’ESISA ou M2 dans des universités étatiques en France suite au M1 à l'ESISA ou Diplôme d'Ingénieur d’état en France (2 ans)</li>
                </ul>
                <p><span class="fw-bold">Le 1er Cycle (Bac +3 ans)</span> , comprenant trois années d'études : 2 années préparatoires et une 3ème année qui est axée sur l’approfondissement des acquis.</p>
                <h2>1ère et 2ème années</h2>
                <p>L'objectif est d'une part d'initier l'étudiant aux domaines de l'informatique avec ses différentes disciplines, et d'autre part de développer ses compétences en développement informatique à travers une formation basée fondamentalement sur la programmation, les structures de données, les langages de programmation, les bases de données et les systèmes d'information ainsi que les réseaux informatiques.</p>
                <h2>3ème année</h2>
                <p>L'objectif est de former des assistants débutants à la maîtrise d’œuvre (MOE) et à la maîtrise d’ouvrage (MO), dans les domaines de la conception et de l’administration des systèmes d’information : bases de données, réseaux, architecture technique, développement d’applications, déploiement logiciel, intégration d’exploitation et analyse et traitements statistiques. <br> <br>
                <span class="fw-bold">Le 2ème Cycle (2 ans après Bac +3)</span> comprend 2 années d’études (4ème et 5ème années d'étude). Il a pour objectif principal de faire évoluer les compétences de l'étudiant vers la maîtrise des grands systèmes et leurs architectures, avec une grande expérience théorique et technique dans le domaine du développement basé sur les nouvelles technologies informatiques. Il se clôt par la réalisation d'un projet de fin d'études dans le secteur industriel, mené principalement au sein des entreprises actives dans le développement informatique à l'échelle internationale et dans le domaine du développement offshore. <br><br>
                L'école ESISA met l'accent sur l'importance de la communication en français et en anglais dans les métiers de l'ingénierie. Tout au long des années d'études, pour les préparer à une meilleure adaptation linguistique dans le cursus final à l'étranger, pour faciliter leur intégration et leur ouvrir les portes des métiers de l'avenir. <br><br>
                En outre les entretiens de sélection pour les différentes écoles et universités partenaires se passent en français et en anglais. Les étudiants de l'ESISA sont appelés à atteindre le niveau de TOEIC (Test Of English for International Communication) en fin de 4ème année.
                
